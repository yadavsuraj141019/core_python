l = [10,20,30,30,40,50,10]

a = l.count(10) # count() batata hai ki list mai konsi value kitni bar repeat ho rhi hai

print(a)

b = max(l)

print(b) # max() means highest value from the list

c = min(l) # min() means lowest value from the list

print(c)

l.sort() # sort() means list ke andar jitne value sab ascending to decending order mai sort ho jayega

print(l)

l.reverse() # reverse() means list mai jitne data sab reverse ho jayega

print(l)


d = l.index(10) # index() means parameter mai aap jo bhi value likhoge uska index number dega aapko...

print(d)


