# dictionary unorder data type
# dictionary works on key and value pair
# dictionary is mutable...


d = {

    'name':'suraj', # name is a 'key' and suraj is the 'value' and key is unique.. ek bar likh diya phirse 'name' key nahi likh sakte...
    'salary': 8000,
    'title':'yadav'
}

print(d['name']) # agar aapko sirf name get karna hai to aap key ki help se kar sakte ho jaise print mai kiya hai...

# display all data with help of for-loop
for n in d:
    print(d[n]) 