#String Indexing

a = "Yadav Suraj"

print(a[6]) #a[6] ka matlab 6 index pe konsa character hai ye pata lagana hai to aap left se chalu karoge to '0' index se chalu hoga matlab '0' index pe 'y' hai

print(a[-5]) #a[-5] ka matlab -5 index pe konsa character hai ye pata lagana hai to aap right se chalu karoge to '-1' index se chalu hoga matlab '-1' index pe 'j' hai



#String Slicing : means aap ko 1 se jada charcter lena ho to slicing ka use karna padega....


print(a[6:11])

print(a[-1::-1])